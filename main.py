@bot.message_handler(commands=['start'])
def start(message):
    markup = InlineKeyboardMarkup()
    for code, data in kitchens.items():
        markup.add(InlineKeyboardButton(data['name'], callback_data=f'kitchen_{code}'))
    bot.send_message(message.chat.id, "👋 Привет! Какая кухня тебя интересует?", reply_markup=markup)


@bot.callback_query_handler(func=lambda call: call.data.startswith('kitchen_'))
def handle_kitchen(call):
    code = call.data.split('_')[1]
    markup = InlineKeyboardMarkup()
    markup.add(
        InlineKeyboardButton('📜 История', callback_data=f'history_{code}'),
        InlineKeyboardButton('📖 Рецепты', callback_data=f'recipes_{code}')
    )
    bot.edit_message_text(
        f'Ты выбрал: {kitchens[code]["name"]}. Что тебя интересует?',
        chat_id=call.message.chat.id,
        message_id=call.message.message_id,
        reply_markup=markup
    )


@bot.callback_query_handler(func=lambda call: call.data.startswith('history_'))
def handle_history(call):
    code = call.data.split('_')[1]
    bot.edit_message_text(
        kitchens[code]['history'],
        chat_id=call.message.chat.id,
        message_id=call.message.message_id
    )


@bot.callback_query_handler(func=lambda call: call.data.startswith('recipes_'))
def handle_recipes(call):
    code = call.data.split('_')[1]
    markup = InlineKeyboardMarkup()
    for dish in kitchens[code]['recipes']:
        markup.add(InlineKeyboardButton(dish, callback_data=f'dish_{code}_{dish}'))
    bot.edit_message_text(
        f'📋 Блюда из {kitchens[code]["name"]}:',
        chat_id=call.message.chat.id,
        message_id=call.message.message_id,
        reply_markup=markup
    )


@bot.callback_query_handler(func=lambda call: call.data.startswith('dish_'))
def handle_dish(call):
    _, code, dish = call.data.split('_', 2)
    recipe = kitchens[code]['recipes'][dish]
    markup = InlineKeyboardMarkup()
    markup.add(InlineKeyboardButton('🎥 Смотреть видео', url=recipe['video']))
    bot.edit_message_text(
        recipe['text'],
        chat_id=call.message.chat.id,
        message_id=call.message.message_id,
        reply_markup=markup
    )


